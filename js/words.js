// Build word lists indexed by length for efficient lookup
var WORDS_BY_LENGTH = {};

(function() {
  // Clean and deduplicate
  const seen = new Set();
  for (const w of WORD_LIST_RAW) {
    const word = w.toUpperCase().trim();
    if (word.length >= 3 && word.length <= 15 && /^[A-Z]+$/.test(word) && !seen.has(word)) {
      seen.add(word);
      const len = word.length;
      if (!WORDS_BY_LENGTH[len]) WORDS_BY_LENGTH[len] = [];
      WORDS_BY_LENGTH[len].push(word);
    }
  }
})();

/**
 * Get all words of a given length.
 */
function getWordsOfLength(len) {
  return WORDS_BY_LENGTH[len] || [];
}

/**
 * Check if a pattern matches a word.
 * Pattern uses uppercase letters for known positions and '.' for unknowns.
 * E.g., "H.LL." matches "HELLO" and "HILLY".
 */
function matchesPattern(word, pattern) {
  if (word.length !== pattern.length) return false;
  for (let i = 0; i < word.length; i++) {
    if (pattern[i] !== '.' && pattern[i] !== word[i]) return false;
  }
  return true;
}

/**
 * Get candidate words matching a pattern.
 */
function getCandidates(pattern) {
  const words = getWordsOfLength(pattern.length);
  return words.filter(w => matchesPattern(w, pattern));
}
